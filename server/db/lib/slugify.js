function slugify(text) {
  if (!text || typeof text !== "string") return "";
  const map = {
    \u0430: "a",
    \u0431: "b",
    \u0432: "v",
    \u0433: "g",
    \u0434: "d",
    \u0435: "e",
    \u0451: "e",
    \u0436: "zh",
    \u0437: "z",
    \u0438: "i",
    \u0439: "y",
    \u043A: "k",
    \u043B: "l",
    \u043C: "m",
    \u043D: "n",
    \u043E: "o",
    \u043F: "p",
    \u0440: "r",
    \u0441: "s",
    \u0442: "t",
    \u0443: "u",
    \u0444: "f",
    \u0445: "kh",
    \u0446: "ts",
    \u0447: "ch",
    \u0448: "sh",
    \u0449: "shch",
    \u044A: "",
    \u044B: "y",
    \u044C: "",
    \u044D: "e",
    \u044E: "yu",
    \u044F: "ya",
    \u0410: "a",
    \u0411: "b",
    \u0412: "v",
    \u0413: "g",
    \u0414: "d",
    \u0415: "e",
    \u0401: "e",
    \u0416: "zh",
    \u0417: "z",
    \u0418: "i",
    \u0419: "y",
    \u041A: "k",
    \u041B: "l",
    \u041C: "m",
    \u041D: "n",
    \u041E: "o",
    \u041F: "p",
    \u0420: "r",
    \u0421: "s",
    \u0422: "t",
    \u0423: "u",
    \u0424: "f",
    \u0425: "kh",
    \u0426: "ts",
    \u0427: "ch",
    \u0428: "sh",
    \u0429: "shch",
    \u042A: "",
    \u042B: "y",
    \u042C: "",
    \u042D: "e",
    \u042E: "yu",
    \u042F: "ya"
  };
  return text.toString().trim().toLowerCase().split("").map((c) => map[c] !== void 0 ? map[c] : c).join("").replace(/\s+/g, "-").replace(/[^a-z0-9-]+/g, "").replace(/-{2,}/g, "-").replace(/^-+|-+$/g, "");
}
module.exports = { slugify };
